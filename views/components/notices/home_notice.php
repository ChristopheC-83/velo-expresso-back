<h4>Pour le slider</h4>
<ul>
    <li>Il faut au moins une photo.</li>
    <li>Les photos sont préférentiellement en 16/9, en paysage, poids max 750 ko.</li>
    <li>pour un rendu sympa sur smartphone, le sujet doit être centré.</li>
    <li>On peut mettre un Overlay pour que la photo soit assombrie, pour mieux voir l'écriture.</li>
    <li>Si on met un lien on met un texte dans le bouton, et vice et versa !</li>
    <li>Le texte, sur 1 ligne sur pc... donc court !</li>

</ul>

